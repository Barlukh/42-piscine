int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 1)
		return (0);
	i = 0;
	while (1)
	{
		if (i * i == nb)
			return (i);
		else if (i * i > nb)
			return (0);
		else
			i++;
	}
}
